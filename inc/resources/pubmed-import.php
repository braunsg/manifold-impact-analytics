<div class="page_header">Importing Publications from PubMed</div>
<div class="faq_container">
	<div class="faq_subheader">Why would I import publications from PubMed?</div>
	<div class="faq_content">
	In some cases, Scopus may not capture the entire publication history of faculty, and as a result, the publication list displayed in Manifold may appear incomplete. In alignment with the Medical School scholarship requirement, faculty have the option of supplementing their Manifold profile with publication data that appears in PubMed but not in Scopus.
	</div>
</div>

<div class="faq_header">Using the importer</div>
<div class="faq_container">
	<a name="search-faculty"></a><div class="faq_subheader">What can I add?</div>
	<div class="faq_content">
	Manifold only covers data for <b>peer-reviewed journal articles</b>. If a peer-reviewed article published by a faculty member cannot be found in Scopus, faculty have the option of importing such data directly from PubMed. <b>Please note</b> that only peer-reviewed journal articles that appear in PubMed but <i>not</i> in Scopus may be imported. Also, the data of publications selected for import from PubMed are pulled automatically and cannot be edited.
	</div>
</div>

<div class="faq_container">
	<a name="search-faculty"></a><div class="faq_subheader">Opening the importer</div>
	<div class="faq_content">
	On your individual faculty profile, click the <b>Import from PubMed</b> link in the upper right-hand corner of the page. This will open a box on your screen with the importer module.
	</div>
</div>

<div class="faq_container">
	<a name="search-faculty"></a><div class="faq_subheader">Searching for articles</div>
	<div class="faq_content">
	Use the search box provided in the opened module to search for specific articles. If you know the PubMed ID for the paper of interest, you may search for the publication directly by importing this ID in the field specified. Otherwise, you may search for your publication by title. Click the <b>Search</b> button to load a list of query results.
	</div>
</div>

<div class="faq_container">
	<a name="search-faculty"></a><div class="faq_subheader">Selecting articles for import</div>
	<div class="faq_content">
	Results of the query provided appear below the search box within the module window. Each row in the list provides data on each publication result, and clicking on the title of any result opens a new window with the original PubMed record. Select your article(s) to import by clicking the checkbox next to the relevant record(s). When you are finished selecting, click the highlighted <b>Import selected publications</b> button.
	</div>
</div>

<div class="faq_container">
	<a name="search-faculty"></a><div class="faq_subheader">Confirming articles for input</div>
	<div class="faq_content">
	After selecting data to import, you will be prompted to confirm the record(s) to be imported. Indicate confirmation by clicking the checkbox next to the relevant publication(s) and click <b>Confirm Submission</b>.
	</div>
</div>

<div class="faq_container">
	<a name="search-faculty"></a><div class="faq_subheader">Approving articles for display</div>
	<div class="faq_content">
	When you have confirmed your submission, you will receive an e-mail to your University of Minnesota e-mail account requesting verification of the imported record(s). Clicking the link in the e-mail verifies that the data will be imported to your profile.
	</div>
</div>
