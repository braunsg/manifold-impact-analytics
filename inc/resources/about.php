<div class="faq_header">Scholarship metrics in the Medical School</div>
<div class="faq_container">
	<a name="why-manifold"></a><div class="faq_subheader">Why was Manifold built?</div>
	<div class="faq_content">
	Manifold was built in response to Dean Brooks Jackson's Scholarship Metrics Initiative and related scholarship requirement. As a system, Manifold harvests publication data from Scopus, including citation counts, to provide metrics of research impact and productivity for all full-time, paid faculty in the Medical School. The profiles generated provide clear demonstrations of the relative impact of researchers across all departments.<br><br>
	In addition, Manifold provides an easy mechanism for identifying nominees for the <a target="_blank" href="http://www.med.umn.edu/research/wall-scholarship">Wall of Scholarship</a>. As more needs arise, the system will continue to be enhanced to meet emerging demands on the available data.
	</div>
</div>
<div class="faq_container">
	<a name="who-built-manifold"></a><div class="faq_subheader">Who built Manifold?</div>
	<div class="faq_content">
	Implementation of Manifold is the result of a collaborative effort between many individuals across the University of Minnesota Libraries. Although developed by the Libraries, the policy and program behind Manifold (the Scholarship Metrics Initiative) is owned by the Medical School. More information can be found in the <b>Attributions</b> section below.
	</div>
</div>

<div class="faq_container">
	<a name="manifold-history"></a><div class="faq_subheader">How long has Manifold been under production?</div>
	<div class="faq_content">
	Development work for Manifold in its current form, including the data presented in it, began in the spring of 2014. The current version of Manifold is the cumulative result of several iterations of modifications and improvements, and all improvements have been implemented as the result of feedback from users of the system (including departmental faculty and department chairs). In particular, several of the improvements made to Manifold are in direct response to a pilot study and usability testing performed in the winter of 2014 with faculty in the Department of Psychiatry.<br><br>
	As the project continues to experience ongoing development, please be advised that content, design, or other facets of the system may change without warning. Your patience is appreciated.
	</div>
</div>

<div class="faq_container">
	<a name="manifold-used"></a><div class="faq_subheader">How is Manifold being used?</div>
	<div class="faq_content">
	The data in Manifold provide measures of the relative breadth, depth, and impact of scholarly work in the Medical School. These data are primarily used for administrative review purposes in alignment with the Scholarship Metrics Initiative, including performance benchmarking and promotion/tenure consideration. However, individual faculty may find the data useful as well. Each faculty and department profile provides a list of (Scopus) publications and metrics that can be easily exported or used for reporting purposes in a variety of settings. Overall, Manifold has been built to serve as a central clearinghouse for publication-based scholarly activity in the Medical School and may have applications in many different contexts.
	</div>
</div>

<div class="faq_container">
	<a name="manifold-not-used"></a><div class="faq_subheader">How is Manifold <b>not</b> being used?</div>
	<div class="faq_content">
	Since Manifold pulls publication data automatically from Scopus, the system does not necessarily provide a perfectly accurate or complete indication of the relative breadth and impact of scholarship for each faculty member in the Medical School. As a result, all data in Manifold are not to be taken as comprehensive but rather as only one demonstrable facet of scholarship out of many others. Having an understanding of the limitations inherent in the data is critical to informed interpretation of the metrics provided; more information on these limitations is available in the <a href="resources.php?p=faq#about-data">Frequently Asked Questions</a>.
	</div>
</div>


<div class="faq_header">University Libraries support for research</div>
<div class="faq_container">
	<a name="why-libraries"></a><div class="faq_subheader">Why are the Libraries providing the data?</div>
	<div class="faq_content">
	Historically, the University Libraries have played a major role in the stewardship of data on campus. As providers of information, the Libraries have a mandate to respond to needs related to data as they arise. Publication data of University of Minnesota faculty fall squarely within the domain of Libraries data stewardship, and Manifold has been built accordingly to facilitate a more streamlined interface with that data.
	</div>
</div>
<div class="faq_container">
	<a name="libraries-help"></a><div class="faq_subheader">How can the Libraries help me?</div>
	<div class="faq_content">
	The University Libraries are committed to providing the resources necessary to better facilitate the research activity of all faculty, staff, and students. As a result, the Libraries provide a variety of services that integrate Libraries staff expertise at all points along the research process. For more information about what is offered, please consult the Libraries' websites on <a target="_blank" href="https://www.lib.umn.edu/researchsupport">researcher support services</a> (especially the resources provided about <a target="_blank" href="https://www.lib.umn.edu/researchsupport/impact">research impact measures</a>) and <a target="_blank" href="https://www.lib.umn.edu/datamanagement">data management</a>. Libraries staff with a variety of subject expertise are available to answer your questions and help you do your research. In addition, many <a target="_blank" href="https://www.lib.umn.edu/services/workshops/registration">workshops</a> are offered throughout the year to help you make your research more efficient, including sessions on securing grant funding for new researchers, managing your research data, and using MNCAT Discovery for finding resources for interdisciplinary research.
	</div>
</div>


